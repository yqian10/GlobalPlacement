
public class module {
		String id;
		double area;
		double x;
		double y;
		int row;
		int col;
		double width;
		double height;
		public module(String id,double area,double x,double y,int row,int col,double width,double height) {
				this.id = id;
				this.area = area;
				this.x = x;
				this.y = y;
				this.row = row;
				this.col = col;
				this.width = width;
				this.height = height;
		}
}
